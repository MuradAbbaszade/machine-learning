import numpy as np
import pandas as pd
from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import train_test_split
from sklearn.metrics import accuracy_score
from sklearn import svm

diabetes_data = pd.read_csv("diabetes.csv")
X=diabetes_data.drop(columns="Outcome",axis=1)
Y=diabetes_data["Outcome"]

# Scale X

scaler = StandardScaler()
scaler.fit(X)
standartized_X=scaler.transform(X)

# Split data to train and test data

X_train,X_test,Y_train,Y_test=train_test_split(standartized_X,Y,test_size=0.2,stratify=Y,random_state=1)

# Train the model

classifier = svm.SVC(kernel="linear")
classifier.fit(X_train,Y_train)

# Model test

train_data_prediction = classifier.predict(X_train)
train_accuracy_score = accuracy_score(train_data_prediction,Y_train)

test_data_prediction = classifier.predict(X_test)
test_accuracy_score = accuracy_score(test_data_prediction,Y_test)

print(train_accuracy_score,test_accuracy_score)

